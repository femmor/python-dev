import math

# Ceil - Rounds up to the next whole number (5)
print("ceil(4.4) =", math.ceil(4.4))

# Floor - Rounds down to the previous whole number (4)
print("floor(4.4) =", math.floor(4.4))

# fabs - Returns the absolute value (4.4)
print("fabs(-4.4) =", math.fabs(-4.4))

# Factorial 1 * 2 * 3 * 4
print("Factorial(4) =", math.factorial(4))

# Fmod - Returns remainder of division (like %)
print("fmod(5, 4) =", math.fmod(5, 4))

# Trunc - Receives a float and returns an int
print("trunc(4.2) =", math.trunc(4.2))

# Pow - Returns x^y
print("pow(2.2) =", math.pow(2, 2))

# Sqrt - Returns the square root
print("sqrt(4) =", math.sqrt(4))

# Special values
print("math.e =", math.e)

print("math.pi =", math.pi)

# Define the base 10^3 = 1000
print("log(1000, 10) =", math.log(1000, 10))

# Trig Functions
# sin, cos, tan, asin, acos, atan, atan2, asinh, acosh, atanh, sinh, cosh, tanh
# They follow this format
print("sin(0) ", math.sin(0))

# Convert radians to degree and vice-versa
print("degrees(1.5708) ", math.degrees(1.5708))
print("radians(90) ", math.radians(90))